/// Convert a tMIR `Ty` to an internal LIR `Type`.
///
/// Signedness is lost in the conversion (tMIR `Int` vs `UInt` both map to `I*`).
/// The signedness is preserved in the opcodes (e.g., `Sdiv` vs `Udiv`).
///
/// For aggregate types (Struct, Array), pass the module's struct definitions
/// via `translate_type_with_structs`.
pub fn translate_type(ty: &Ty) -> Result<Type, AdapterError> {
    translate_type_with_structs(ty, &[])
}

/// Convert a tMIR `Ty` to an internal LIR `Type`, with struct definitions for
/// resolving `Ty::Struct(StructId)` references.
pub fn translate_type_with_structs(
    ty: &Ty,
    structs: &[StructDef],
) -> Result<Type, AdapterError> {
    match ty {
        Ty::Bool => Ok(Type::B1),
        Ty::I8 => Ok(Type::I8),
        Ty::I16 => Ok(Type::I16),
        Ty::I32 => Ok(Type::I32),
        Ty::I64 => Ok(Type::I64),
        Ty::I128 => Ok(Type::I128),
        Ty::F32 => Ok(Type::F32),
        Ty::F64 => Ok(Type::F64),
        Ty::Void => Err(AdapterError::VoidValue),
        Ty::Ptr => Ok(Type::I64), // All pointers are 64-bit on AArch64
        Ty::Struct(sid) => {
            // Look up the struct definition and translate each field type.
            let sdef = structs
                .iter()
                .find(|s| s.id == *sid)
                .ok_or_else(|| AdapterError::UnsupportedType(format!("unknown struct {:?}", sid)))?;
            let field_types: Vec<Type> = sdef
                .fields
                .iter()
                .map(|f| translate_type_with_structs(&f.ty, structs))
                .collect::<Result<Vec<_>, _>>()?;
            Ok(Type::Struct(field_types))
        }
        Ty::Array(elem_ty_id, len) => {
            // Array uses TyId — for now, treat as unsupported since we'd need the
            // module's type table to resolve TyId. This can be extended later.
            Err(AdapterError::UnsupportedType(format!("Array(TyId({}), {})", elem_ty_id.index(), len)))
        }
        Ty::Func(_) => {
            Err(AdapterError::UnsupportedType(format!("{:?}", ty)))
        }
    }
}

/// Convert a tMIR `Ty` to an internal LIR `Type`, with a module type table for
/// resolving `Ty::Array(TyId, len)` references.
///
/// Unlike [`translate_type`] which returns `Err` for array types, this variant
/// uses the module's `types: Vec<Ty>` table to look up the element type behind
/// a `TyId` and recursively translate it. This is essential for the computation
/// graph builder which needs `Type::Array` entries in `SubgraphDescriptor` so
/// that `operates_on_arrays()` returns `true` and GPU/ANE legality is evaluated.
pub fn translate_type_with_type_table(
    ty: &Ty,
    type_table: &[Ty],
) -> Result<Type, AdapterError> {
    match ty {
        Ty::Array(elem_ty_id, len) => {
            let elem_ty = type_table
                .get(elem_ty_id.index() as usize)
                .ok_or_else(|| {
                    AdapterError::UnsupportedType(format!(
                        "Array(TyId({}), {})",
                        elem_ty_id.index(),
                        len,
                    ))
                })?;
            let lir_elem = translate_type_with_type_table(elem_ty, type_table)?;
            Ok(Type::Array(Box::new(lir_elem), *len as u32))
        }
        _ => translate_type(ty),
    }
}

/// Convert a tMIR `Ordering` to an internal LIR `AtomicOrdering`.
fn translate_ordering(ordering: &TmirOrdering) -> AtomicOrdering {
    match ordering {
        TmirOrdering::Relaxed => AtomicOrdering::Relaxed,
        TmirOrdering::Acquire => AtomicOrdering::Acquire,
        TmirOrdering::Release => AtomicOrdering::Release,
        TmirOrdering::AcqRel => AtomicOrdering::AcqRel,
        TmirOrdering::SeqCst => AtomicOrdering::SeqCst,
    }
}

/// Convert a tMIR `AtomicRMWOp` to an internal LIR `AtomicRmwOp`.
fn translate_atomic_rmw_op(op: &TmirAtomicRmwOp) -> AtomicRmwOp {
    match op {
        TmirAtomicRmwOp::Add => AtomicRmwOp::Add,
        TmirAtomicRmwOp::Sub => AtomicRmwOp::Sub,
        TmirAtomicRmwOp::And => AtomicRmwOp::And,
        TmirAtomicRmwOp::Or => AtomicRmwOp::Or,
        TmirAtomicRmwOp::Xor => AtomicRmwOp::Xor,
        TmirAtomicRmwOp::Xchg => AtomicRmwOp::Xchg,
        // New variants in real tmir not yet supported by LIR
        TmirAtomicRmwOp::Max | TmirAtomicRmwOp::Min
        | TmirAtomicRmwOp::UMax | TmirAtomicRmwOp::UMin => {
            // TODO(#283): Add Max/Min/UMax/UMin support to LIR AtomicRmwOp
            AtomicRmwOp::Add // placeholder
        }
    }
}

/// Translate a tMIR function signature to an internal LIR `Signature`.
///
/// Since the new tmir crate stores function types by FuncTyId reference,
/// we need the module's func_types table to resolve the actual FuncTy.
fn translate_signature(func: &TmirFunction, module: &TmirModule) -> Result<Signature, AdapterError> {
    let func_ty = &module.func_types[func.ty.index() as usize];
    let params: Vec<Type> = func_ty
        .params
        .iter()
        .map(translate_type)
        .collect::<Result<Vec<_>, _>>()?;
    let returns: Vec<Type> = func_ty
        .returns
        .iter()
        .map(translate_type)
        .collect::<Result<Vec<_>, _>>()?;
    Ok(Signature { params, returns })
}

