    fn translate_br(
        &mut self,
        target: &BlockId,
        args: &[ValueId],
    ) -> Result<Vec<Instruction>, AdapterError> {
        let mut instrs = Vec::new();

        // Emit copies for block arguments -> block parameters.
        instrs.extend(self.resolve_block_args(*target, args)?);

        let dest = self.map_block(*target);
        instrs.push(Instruction {
            opcode: Opcode::Jump { dest },
            args: vec![],
            results: vec![],
        });

        Ok(instrs)
    }

    fn translate_condbr(
        &mut self,
        cond: &ValueId,
        then_target: &BlockId,
        then_args: &[ValueId],
        else_target: &BlockId,
        else_args: &[ValueId],
    ) -> Result<Vec<Instruction>, AdapterError> {
        let mut instrs = Vec::new();

        // Emit copies for then-branch block arguments.
        instrs.extend(self.resolve_block_args(*then_target, then_args)?);

        // Emit copies for else-branch block arguments.
        instrs.extend(self.resolve_block_args(*else_target, else_args)?);

        let cond_val = self.map_value(*cond);
        let then_dest = self.map_block(*then_target);
        let else_dest = self.map_block(*else_target);

        instrs.push(Instruction {
            opcode: Opcode::Brif {
                cond: cond_val,
                then_dest,
                else_dest,
            },
            args: vec![cond_val],
            results: vec![],
        });

        Ok(instrs)
    }

