// Translates tMIR (the universal IR from tRust/tSwift/tC) to the internal LIR
// consumed by the ISel. This adapter approach (Option A in the design doc)
// decouples the ISel from tMIR API changes while preserving proof annotations
// as metadata for downstream optimization passes.

//! tMIR to internal LIR adapter layer.
//!
//! This module translates `tmir_func::Module` / `tmir_func::Function` into the
//! internal `llvm2_lower::function::Function` representation, mapping each tMIR
//! instruction to one or more internal `Instruction`s.
//!
//! Proof annotations from tMIR are extracted into a `ProofContext` for consumption
//! by downstream optimization passes (llvm2-opt).

use std::collections::HashMap;

use tmir_func::{Block as TmirBlock, Function as TmirFunction, Module as TmirModule};
use tmir_instrs::{AtomicRmwOp as TmirAtomicRmwOp, BinOp, CastOp, CmpOp, Instr, InstrNode, MemoryOrdering, SwitchCase, UnOp};
use tmir_types::{BlockId, FuncId, StructDef, TmirProof, Ty, ValueId};

use crate::function::{BasicBlock, Function, Signature};
use crate::instructions::{AtomicOrdering, AtomicRmwOp, Block, FloatCC, Instruction, IntCC, Opcode, Value};
use crate::types::Type;

// ---------------------------------------------------------------------------
// Error types
// ---------------------------------------------------------------------------

/// Errors that can occur during tMIR to LIR translation.
#[derive(Debug, Clone, thiserror::Error)]
pub enum AdapterError {
    #[error("unsupported tMIR type: {0}")]
    UnsupportedType(String),

    #[error("void type used as value")]
    VoidValue,

    #[error("instruction has no result but one was expected")]
    MissingResult,

    #[error("unsupported tMIR instruction: {0}")]
    UnsupportedInstruction(String),

    #[error("unknown block reference: BlockId({0})")]
    UnknownBlock(u32),

    #[error("unknown value reference: ValueId({0})")]
    UnknownValue(u32),

    #[error("unsupported integer bit-width: {0}")]
    UnsupportedBitWidth(u16),
}

// ---------------------------------------------------------------------------
// Proof annotations
// ---------------------------------------------------------------------------

/// Proof annotations attached to tMIR values/instructions.
///
/// These proofs are generated by the source-language compiler (tRust, tSwift, tC)
/// and verified by z4. The adapter extracts them for downstream optimization.
#[derive(Debug, Clone, PartialEq)]
pub enum Proof {
    /// Addition/subtraction guaranteed not to overflow.
    /// Enables: skip overflow check, use wrapping arithmetic directly.
    NoOverflow { signed: bool },

    /// Array/pointer access is within bounds.
    /// Enables: skip bounds check, use direct load/store.
    InBounds { base: ValueId, index: ValueId },

    /// Pointer is guaranteed non-null.
    /// Enables: skip null check.
    NotNull { ptr: ValueId },

    /// Borrow is guaranteed valid (lifetime within scope).
    /// Enables: skip liveness check, treat as raw pointer.
    ValidBorrow { borrow: ValueId },

    /// Value is within a specific range [lo, hi].
    /// Enables: range-based optimizations (width narrowing, skip sign extension).
    InRange { lo: i128, hi: i128 },

    /// Division divisor is guaranteed non-zero.
    /// Enables: skip divide-by-zero check.
    NonZeroDivisor { divisor: ValueId },

    /// Shift amount is within [0, bitwidth).
    /// Enables: skip shift-amount masking.
    ValidShift { amount: ValueId, bitwidth: u16 },

    /// Function or computation has no side effects.
    /// Enables: aggressive CSE, LICM, GPU/ANE dispatch.
    Pure,

    /// Operation is associative: (a op b) op c = a op (b op c).
    /// Enables: parallel reduction, operation reordering.
    Associative,

    /// Operation is commutative: a op b = b op a.
    /// Enables: parallel reduction, operand reordering.
    Commutative,

    /// Operation is idempotent: f(f(x)) = f(x).
    /// Enables: redundant application elimination.
    Idempotent,
}

/// Proof metadata carried through the compilation pipeline.
///
/// After the adapter translates tMIR to internal LIR, proof annotations are
/// re-keyed from `ValueId` to internal `Value` references so downstream
/// passes can query them.
#[derive(Debug, Clone, Default)]
pub struct ProofContext {
    /// Proofs attached to specific internal LIR values (re-keyed from tMIR ValueId).
    pub value_proofs: HashMap<Value, Vec<Proof>>,
    /// Function-level proofs (apply to the entire function, not individual values).
    /// These come from tMIR `Function::proofs` and include Pure, Associative,
    /// Commutative annotations that describe whole-function properties.
    pub function_proofs: Vec<Proof>,
}

impl ProofContext {
    /// Check if a value has a specific proof type.
    pub fn has_no_overflow(&self, val: &Value) -> bool {
        self.value_proofs
            .get(val)
            .is_some_and(|proofs| proofs.iter().any(|p| matches!(p, Proof::NoOverflow { .. })))
    }

    /// Check if a value has a non-null proof.
    pub fn has_not_null(&self, val: &Value) -> bool {
        self.value_proofs
            .get(val)
            .is_some_and(|proofs| proofs.iter().any(|p| matches!(p, Proof::NotNull { .. })))
    }

    /// Check if a value has an in-bounds proof.
    pub fn has_in_bounds(&self, val: &Value) -> bool {
        self.value_proofs
            .get(val)
            .is_some_and(|proofs| proofs.iter().any(|p| matches!(p, Proof::InBounds { .. })))
    }

    /// Check if a value has a non-zero divisor proof.
    pub fn has_non_zero_divisor(&self, val: &Value) -> bool {
        self.value_proofs.get(val).is_some_and(|proofs| {
            proofs
                .iter()
                .any(|p| matches!(p, Proof::NonZeroDivisor { .. }))
        })
    }

    /// Get range information for a value, if available.
    pub fn get_range(&self, val: &Value) -> Option<(i128, i128)> {
        self.value_proofs.get(val).and_then(|proofs| {
            proofs.iter().find_map(|p| match p {
                Proof::InRange { lo, hi } => Some((*lo, *hi)),
                _ => None,
            })
        })
    }

    /// Check if a value has a valid borrow proof.
    pub fn has_valid_borrow(&self, val: &Value) -> bool {
        self.value_proofs
            .get(val)
            .is_some_and(|proofs| proofs.iter().any(|p| matches!(p, Proof::ValidBorrow { .. })))
    }

    /// Check if a value has a valid shift proof.
    pub fn has_valid_shift(&self, val: &Value) -> bool {
        self.value_proofs
            .get(val)
            .is_some_and(|proofs| proofs.iter().any(|p| matches!(p, Proof::ValidShift { .. })))
    }

    /// Check if a value has a pure proof (no side effects).
    pub fn has_pure(&self, val: &Value) -> bool {
        self.value_proofs
            .get(val)
            .is_some_and(|proofs| proofs.iter().any(|p| matches!(p, Proof::Pure)))
    }

    /// Check if a value has an associative proof.
    pub fn has_associative(&self, val: &Value) -> bool {
        self.value_proofs
            .get(val)
            .is_some_and(|proofs| proofs.iter().any(|p| matches!(p, Proof::Associative)))
    }

    /// Check if a value has a commutative proof.
    pub fn has_commutative(&self, val: &Value) -> bool {
        self.value_proofs
            .get(val)
            .is_some_and(|proofs| proofs.iter().any(|p| matches!(p, Proof::Commutative)))
    }

    /// Check if a value has an idempotent proof.
    pub fn has_idempotent(&self, val: &Value) -> bool {
        self.value_proofs
            .get(val)
            .is_some_and(|proofs| proofs.iter().any(|p| matches!(p, Proof::Idempotent)))
    }

    /// Get all proofs attached to a value.
    pub fn proofs_for(&self, val: &Value) -> &[Proof] {
        self.value_proofs
            .get(val)
            .map(|v| v.as_slice())
            .unwrap_or(&[])
    }

    /// Add a function-level proof.
    pub fn add_function_proof(&mut self, proof: Proof) {
        self.function_proofs.push(proof);
    }

    /// Check if the function has a pure proof.
    pub fn is_function_pure(&self) -> bool {
        self.function_proofs.iter().any(|p| matches!(p, Proof::Pure))
    }

    /// Check if the function has an associative proof.
    pub fn is_function_associative(&self) -> bool {
        self.function_proofs
            .iter()
            .any(|p| matches!(p, Proof::Associative))
    }

    /// Check if the function has a commutative proof.
    pub fn is_function_commutative(&self) -> bool {
        self.function_proofs
            .iter()
            .any(|p| matches!(p, Proof::Commutative))
    }

    /// Check if the function has an idempotent proof.
    pub fn is_function_idempotent(&self) -> bool {
        self.function_proofs
            .iter()
            .any(|p| matches!(p, Proof::Idempotent))
    }

    /// Get all function-level proofs.
    pub fn function_proofs(&self) -> &[Proof] {
        &self.function_proofs
    }
}

// ---------------------------------------------------------------------------
// Type translation
// ---------------------------------------------------------------------------

/// Convert a tMIR `Ty` to an internal LIR `Type`.
///
/// Signedness is lost in the conversion (tMIR `Int` vs `UInt` both map to `I*`).
/// The signedness is preserved in the opcodes (e.g., `Sdiv` vs `Udiv`).
///
/// For aggregate types (Struct, Array), pass the module's struct definitions
/// via `translate_type_with_structs`.
pub fn translate_type(ty: &Ty) -> Result<Type, AdapterError> {
    translate_type_with_structs(ty, &[])
}

/// Convert a tMIR `Ty` to an internal LIR `Type`, with struct definitions for
/// resolving `Ty::Struct(StructId)` references.
pub fn translate_type_with_structs(
    ty: &Ty,
    structs: &[StructDef],
) -> Result<Type, AdapterError> {
    match ty {
        Ty::Bool => Ok(Type::B1),
        Ty::Int(8) | Ty::UInt(8) => Ok(Type::I8),
        Ty::Int(16) | Ty::UInt(16) => Ok(Type::I16),
        Ty::Int(32) | Ty::UInt(32) => Ok(Type::I32),
        Ty::Int(64) | Ty::UInt(64) => Ok(Type::I64),
        Ty::Int(128) | Ty::UInt(128) => Ok(Type::I128),
        Ty::Float(32) => Ok(Type::F32),
        Ty::Float(64) => Ok(Type::F64),
        Ty::Ptr(_) => Ok(Type::I64), // All pointers are 64-bit on AArch64
        Ty::Void => Err(AdapterError::VoidValue),
        Ty::Int(w) | Ty::UInt(w) | Ty::Float(w) => {
            Err(AdapterError::UnsupportedBitWidth(*w))
        }
        Ty::Struct(sid) => {
            // Look up the struct definition and translate each field type.
            let sdef = structs
                .iter()
                .find(|s| s.id == *sid)
                .ok_or_else(|| AdapterError::UnsupportedType(format!("unknown struct {:?}", sid)))?;
            let field_types: Vec<Type> = sdef
                .fields
                .iter()
                .map(|f| translate_type_with_structs(&f.ty, structs))
                .collect::<Result<Vec<_>, _>>()?;
            Ok(Type::Struct(field_types))
        }
        Ty::Array(elem_ty, count) => {
            let elem = translate_type_with_structs(elem_ty, structs)?;
            Ok(Type::Array(Box::new(elem), *count as u32))
        }
        Ty::Func(_) => {
            Err(AdapterError::UnsupportedType(format!("{:?}", ty)))
        }
    }
}

/// Convert a tMIR `MemoryOrdering` to an internal LIR `AtomicOrdering`.
fn translate_ordering(ordering: &MemoryOrdering) -> AtomicOrdering {
    match ordering {
        MemoryOrdering::Relaxed => AtomicOrdering::Relaxed,
        MemoryOrdering::Acquire => AtomicOrdering::Acquire,
        MemoryOrdering::Release => AtomicOrdering::Release,
        MemoryOrdering::AcqRel => AtomicOrdering::AcqRel,
        MemoryOrdering::SeqCst => AtomicOrdering::SeqCst,
    }
}

/// Convert a tMIR `AtomicRmwOp` to an internal LIR `AtomicRmwOp`.
fn translate_atomic_rmw_op(op: &TmirAtomicRmwOp) -> AtomicRmwOp {
    match op {
        TmirAtomicRmwOp::Add => AtomicRmwOp::Add,
        TmirAtomicRmwOp::Sub => AtomicRmwOp::Sub,
        TmirAtomicRmwOp::And => AtomicRmwOp::And,
        TmirAtomicRmwOp::Or => AtomicRmwOp::Or,
        TmirAtomicRmwOp::Xor => AtomicRmwOp::Xor,
        TmirAtomicRmwOp::Xchg => AtomicRmwOp::Xchg,
    }
}

/// Translate a tMIR function signature to an internal LIR `Signature`.
fn translate_signature(func: &TmirFunction) -> Result<Signature, AdapterError> {
    let params: Vec<Type> = func
        .ty
        .params
        .iter()
        .map(|ty| translate_type(ty))
        .collect::<Result<Vec<_>, _>>()?;
    let returns: Vec<Type> = func
        .ty
        .returns
        .iter()
        .map(|ty| translate_type(ty))
        .collect::<Result<Vec<_>, _>>()?;
    Ok(Signature { params, returns })
}

