    fn translate_cast(
        &mut self,
        op: &CastOp,
        src_ty: &Ty,
        dst_ty: &Ty,
        operand: &ValueId,
        results: &[ValueId],
    ) -> Result<Vec<Instruction>, AdapterError> {
        let src = self.map_value(*operand);
        let dst = self.map_result(results)?;
        let from = self.translate_ty(src_ty)?;
        let to = self.translate_ty(dst_ty)?;

        let opcode = match op {
            CastOp::SExt => Opcode::Sextend {
                from_ty: from,
                to_ty: to,
            },
            CastOp::ZExt => Opcode::Uextend {
                from_ty: from,
                to_ty: to,
            },
            CastOp::Trunc => Opcode::Trunc { to_ty: to },
            CastOp::FPToSI => Opcode::FcvtToInt { dst_ty: to },
            CastOp::FPToUI => Opcode::FcvtToUint { dst_ty: to },
            CastOp::SIToFP => Opcode::FcvtFromInt { src_ty: from },
            CastOp::UIToFP => Opcode::FcvtFromUint { src_ty: from },
            CastOp::FPExt => Opcode::FPExt,
            CastOp::FPTrunc => Opcode::FPTrunc,
            CastOp::PtrToInt | CastOp::IntToPtr => {
                // No-op: pointers are already I64. Emit a single Copy pseudo.
                return Ok(vec![Instruction {
                    opcode: Opcode::Copy,
                    args: vec![src],
                    results: vec![dst],
                }]);
            }
            CastOp::Bitcast => Opcode::Bitcast { to_ty: to },
        };

        Ok(vec![Instruction {
            opcode,
            args: vec![src],
            results: vec![dst],
        }])
    }

